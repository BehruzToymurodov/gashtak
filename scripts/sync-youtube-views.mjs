import { readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const rootDir = path.join(__dirname, '..')
const episodesPath = path.join(rootDir, 'src/data/episodes.ts')
const generatedPath = path.join(rootDir, 'src/data/youtubeViews.generated.ts')

const apiKey = process.env.YOUTUBE_API_KEY

function extractEpisodeData(source) {
	const blockRegex =
		/\{\s*ep:\s*(\d+),\s*youtubeId:\s*'([^']+)',[\s\S]*?views:\s*(\d+),\s*\}/g
	const episodes = []
	let match
	while ((match = blockRegex.exec(source)) !== null) {
		episodes.push({
			ep: Number(match[1]),
			youtubeId: match[2],
			views: Number(match[3]),
		})
	}
	return episodes
}

async function fetchRealViews(ids) {
	if (!apiKey) return {}

	const out = {}
	const chunkSize = 50

	for (let i = 0; i < ids.length; i += chunkSize) {
		const chunk = ids.slice(i, i + chunkSize)
		const url = new URL('https://www.googleapis.com/youtube/v3/videos')
		url.searchParams.set('part', 'statistics')
		url.searchParams.set('id', chunk.join(','))
		url.searchParams.set('key', apiKey)

		const res = await fetch(url)
		if (!res.ok) {
			console.warn(
				`YouTube API request failed (${res.status} ${res.statusText}); using fallback counts.`,
			)
			return {}
		}

		const data = await res.json()
		for (const item of data.items ?? []) {
			const id = item.id
			const viewCount = Number(item?.statistics?.viewCount ?? 0)
			if (id) out[id] = Number.isFinite(viewCount) ? viewCount : 0
		}
	}

	return out
}

async function main() {
	const source = await readFile(episodesPath, 'utf8')
	const episodes = extractEpisodeData(source)

	if (episodes.length === 0) {
		throw new Error('No episodes found in src/data/episodes.ts')
	}

	const ids = episodes.map(e => e.youtubeId)
	const realViews = await fetchRealViews(ids)

	const merged = Object.fromEntries(
		episodes.map(ep => [ep.youtubeId, realViews[ep.youtubeId] ?? ep.views]),
	)

	const generated = `// This file is generated by scripts/sync-youtube-views.mjs\n// Do not edit manually. Run \`npm run sync:views\` to refresh.\n\nexport const YOUTUBE_VIEWS: Record<string, number> = ${JSON.stringify(merged, null, 2)}\n`

	await writeFile(generatedPath, generated, 'utf8')
	console.log(
		`Updated ${path.relative(rootDir, generatedPath)} (${apiKey ? 'real views' : 'fallback views'})`,
	)
}

main().catch(err => {
	console.error(err instanceof Error ? err.message : err)
	process.exit(1)
})
